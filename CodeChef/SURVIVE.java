package CodeChef;

import java.util.*;

public class SURVIVE {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for (int i = 0; i < t; i++) {
            double n = sc.nextInt();
            double k = sc.nextInt();
            double s = sc.nextInt();
            double c = 0;
            while(c>k*s)
            {
                c = c+n;
            }
        }
    }
}
