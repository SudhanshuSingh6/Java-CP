package CodeForces;

import java.util.*;

public class SpecificTastesOfAndre {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for (int x = 0; x < t; x++) {
            int n = sc.nextInt();
            for (int i = 0; i < n; i++) {
                System.out.print(1 + " ");
            }
            System.out.println();
        }
    }
}
